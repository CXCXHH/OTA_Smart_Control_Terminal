void OLED_ShowStr(uint8_t x, uint8_t y, const char ch[], uint8_t size)
{
    uint8_t c, i;
    while (*ch)
    {
        c = *ch - 32;
        if (c > 94) c = 0;

        if (size == 0)  /* 6x8 font */
        {
            if (x > 126) { x = 0; y++; }
            OLED_SetPos(x, y);
            for (i = 0; i < 6; i++)
                WriteDat(F6x8[c][i]);
            x += 6;
        }
        else  /* 8x16 font */
        {
            if (x > 120) { x = 0; y += 2; }
            OLED_SetPos(x, y);
            for (i = 0; i < 8; i++)
                WriteDat(F8x16[c][i]);
            OLED_SetPos(x, y + 1);
            for (i = 0; i < 8; i++)
                WriteDat(F8x16[c][8 + i]);
            x += 8;
        }
        ch++;
    }
}
